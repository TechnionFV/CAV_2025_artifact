use super::Utils;

// ************************************************************************************************
// impl
// ************************************************************************************************

impl Utils {
    pub fn is_sorted<T>(data: &[T]) -> bool
    where
        T: Ord,
    {
        data.windows(2).all(|w| w[0] <= w[1])
    }

    pub fn is_sorted_and_unique<T>(data: &[T]) -> bool
    where
        T: Ord,
    {
        data.windows(2).all(|w| w[0] < w[1])
    }
}
