use crate::models::unique_sorted_hash_map::UniqueSortedHash;

// ************************************************************************************************
// struct
// ************************************************************************************************

pub struct BtorLine {
    i: usize,
}

// ************************************************************************************************
// UniqueSortedHash
// ************************************************************************************************

impl UniqueSortedHash for BtorLine {
    fn hash(&self) -> usize {
        self.i
    }

    fn un_hash(i: usize) -> Self {
        Self { i }
    }
}
