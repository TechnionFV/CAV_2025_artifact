use super::UniqueSortedVec;
use std::fmt::{self, Display};

// ************************************************************************************************
// Ordering
// ************************************************************************************************

impl<V: Ord + Display> Display for UniqueSortedVec<V> {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "[")?;
        for (i, v) in self.usv.iter().enumerate() {
            if i != 0 {
                write!(f, ", ")?;
            }
            write!(f, "{}", v)?;
        }
        write!(f, "]")
    }
}
